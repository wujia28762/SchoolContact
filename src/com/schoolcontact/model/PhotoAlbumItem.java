package com.schoolcontact.model;

import java.util.List;

public class PhotoAlbumItem {

	private String TimeStr;
	private List<FriendMessageInfo> mSubList;

	public PhotoAlbumItem(String timeStr, List<FriendMessageInfo> mSubList) {

		TimeStr = timeStr;
		this.mSubList = mSubList;
	}

	public List<FriendMessageInfo> getmSubList() {
		return mSubList;
	}

	public void setmSubList(List<FriendMessageInfo> mSubList) {
		this.mSubList = mSubList;
	}

	public String getTimeStr() {
		return TimeStr;
	}

	public void setTimeStr(String timeStr) {
		TimeStr = timeStr;
	}

}
