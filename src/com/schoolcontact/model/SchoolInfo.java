package com.schoolcontact.model;

import java.io.Serializable;
import java.util.List;

public class SchoolInfo implements Serializable {
	/**
	 * @description 
	 */
	private static final long serialVersionUID = -8765473326466926449L;
	/**
	 * @description 
	 */
	private String id;
	private String name;
	private String isGroup;
	private String groupId;
	private String portraitUri;
	private List<ClassInfo> contents;

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getIsGroup() {
		return isGroup;
	}

	public void setIsGroup(String isGroup) {
		this.isGroup = isGroup;
	}

	public String getGroupId() {
		return groupId;
	}

	public void setGroupId(String groupId) {
		this.groupId = groupId;
	}



	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public List<ClassInfo> getContents() {
		return contents;
	}

	public void setContents(List<ClassInfo> contents) {
		this.contents = contents;
	}

	public String getPortraitUri() {
		return portraitUri;
	}

	public void setPortraitUri(String portraitUri) {
		this.portraitUri = portraitUri;
	}

}
