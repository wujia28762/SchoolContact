package com.schoolcontact.model;

public class ReturnMessage {

	public Object getData() {
		return data;
	}
	public void setData(Object data) {
		this.data = data;
	}
	private String code;
	private String mess;
	private int event;
	private Object data; 

	public ReturnMessage(String code, String mess, int event,Object data) {
		this.code = code;
		this.event = event;
		this.mess = mess;
		this.data = data;
	}
	public int getEvent() {
		return event;
	}
	public void setEvent(int event) {
		this.event = event;
	}
	public String getMess() {
		return mess;
	}
	public void setMess(String mess) {
		this.mess = mess;
	}
	public String getCode() {
		return code;
	}
	public void setCode(String code) {
		this.code = code;
	}
	
}
